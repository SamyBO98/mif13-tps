class Zrr {

    constructor(no, ne, se, so){
        this.no = no;
        this.ne = ne;
        this.se = se;
        this.so = so;
    }

    getNo(){
        return this.no;
    }

    getNe(){
        return this.ne;
    }

    getSe(){
        return this.se;
    }

    getSo(){
        return this.so;
    }

} module.exports.class = Zrr